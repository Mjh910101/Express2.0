public class HelpAdapter extends BaseAdapter {

    private Context context;
    private List<HelpObj> imageList;
    private LayoutInflater inflater;

    public HelpAdapter(Context context, List<HelpObj> imageList) {
        initBaseAdapter(context);
        this.imageList = imageList;
    }

    private void initBaseAdapter(Context context) {
        this.context = context;
        inflater = (LayoutInflater) context
                .getSystemService(Context.LAYOUT_INFLATER_SERVICE);
    }

    @Override
    public int getCount() {
        return imageList.size();
    }

    @Override
    public Object getItem(int position) {
        return imageList.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {

        HelpObj obj = imageList.get(position);
        int w = WinTool.getWinWidth(context) / 4;

        ImageView view = new ImageView(context);
        view.setLayoutParams(new GridView.LayoutParams(w, w));
        DownloadImageLoader.loadImage(view, obj.getImg());

        return view;
    }
}
